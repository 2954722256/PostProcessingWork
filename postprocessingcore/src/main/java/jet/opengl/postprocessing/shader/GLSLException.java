package jet.opengl.postprocessing.shader;

public class GLSLException extends RuntimeException{

	private static final long serialVersionUID = 4826926336407644190L;

	public GLSLException() {
	}

	public GLSLException(String message, Throwable cause) {
		super(message, cause);
	}

	public GLSLException(String message) {
		super(message);
	}

	public GLSLException(Throwable cause) {
		super(cause);
	}

	
}
