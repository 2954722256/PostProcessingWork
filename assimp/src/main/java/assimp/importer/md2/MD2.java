	// Helper function to lookup a normal in Quake 2's precalculated table
	public static void lookupNormalIndex(int iNormalIndex,Vector3f vOut)
	{
		// make sure the normal index has a valid value
		if (iNormalIndex >= g_avNormals.length)	{
			if(DefaultLogger.LOG_OUT)
				DefaultLogger.warn("Index overflow in Quake II normal vector list");
			iNormalIndex = g_avNormals.length - 1;
		}
//		vOut = *((const aiVector3D*)(&g_avNormals[iNormalIndex]));
		vOut.load(g_avNormals[iNormalIndex], 0);
	}
